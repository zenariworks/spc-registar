def rand_first_name(gender: str) -> str:
    """gender ∈ {"М", "Ж"} (matches Parohijan.pol values)."""
    pool = MALE_NAMES if gender == "М" else FEMALE_NAMES
    return random.choice(pool)


def rand_surname() -> str:
    return random.choice(SURNAMES)


def rand_gender() -> str:
    return random.choice(("М", "Ж"))


def rand_place() -> str:
    return random.choice(PLACES)


def rand_street() -> str:
    return random.choice(STREETS)


def rand_phone(mobile: bool = True) -> str:
    """Returns a Serbian-format phone string."""
    if mobile:
        # +381 6X XXX XXXX
        return f"+381 6{random.randint(0, 9)} {random.randint(100, 999)} {random.randint(1000, 9999)}"
    # +381 1X XXX XXXX  (Belgrade landline-ish)
    return f"+381 {random.randint(11, 32)} {random.randint(100, 999)} {random.randint(1000, 9999)}"


def rand_postcode() -> str:
    return f"{random.randint(11, 38)}000"


# --------------------------------------------------------------------
# AGE / DATE GENERATORS (bimodal — children + adults + elderly)
# --------------------------------------------------------------------
TODAY = date.today()


def rand_birthdate_in_age_range(min_age: int, max_age: int) -> date:
    """Uniform within an explicit age range. Inclusive."""
    days_min = min_age * 365
    days_max = max_age * 365 + 364
    days_back = random.randint(days_min, days_max)
    return TODAY - timedelta(days=days_back)


def rand_birthdate_parishioner() -> date:
    """Realistic Serbian Orthodox parohija age distribution.

    Bimodal: 25% children (0-17), 55% adults (18-65), 20% elderly (65-95).
    """
    r = random.random()
    if r < 0.25:
        return rand_birthdate_in_age_range(0, 17)
    if r < 0.80:
        return rand_birthdate_in_age_range(18, 65)
    return rand_birthdate_in_age_range(65, 95)


def rand_birthdate_priest() -> date:
    """Priests are male adults, 25-80 (per constraints)."""
    return rand_birthdate_in_age_range(25, 80)


def rand_birthdate_child() -> date:
    """For baptism — child is 0-5 years."""
    return rand_birthdate_in_age_range(0, 5)


def rand_birthdate_spouse_near(other: date, max_diff_years: int = 12) -> date:
    """Generate a birthdate within ±max_diff_years of `other`."""
    diff = random.randint(-max_diff_years * 365, max_diff_years * 365)
    return other + timedelta(days=diff)


def rand_date_between(start: date, end: date) -> date:
    """Inclusive."""
    delta = (end - start).days
    return start + timedelta(days=random.randint(0, delta))


def age(birthdate: date, on: date | None = None) -> int:
    on = on or TODAY
    y = on.year - birthdate.year
    if (on.month, on.day) < (birthdate.month, birthdate.day):
        y -= 1
    return y
