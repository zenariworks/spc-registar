"""Филтер: редни број (број → реч у средњем роду) за приказ „по реду“."""

from django import template

register = template.Library()

_REDNI = {
    1: "прво", 2: "друго", 3: "треће", 4: "четврто", 5: "пето",
    6: "шесто", 7: "седмо", 8: "осмо", 9: "девето", 10: "десето",
    11: "једанаесто", 12: "дванаесто", 13: "тринаесто", 14: "четрнаесто",
    15: "петнаесто", 16: "шеснаесто", 17: "седамнаесто", 18: "осамнаесто",
    19: "деветнаесто", 20: "двадесето",
}

NIJE_UPISANO = "није уписано"


@register.filter
def redni_rec(value):
    """Број → редни број речима у средњем роду (1 → „прво“, 2 → „друго“…).

    Празно/None или вредност мања од 1 → „није уписано“ (експлицитна ознака
    да податак није забележен). Ван опсега 1–20 враћа „N.“ (нпр. 21 → „21.“).
    Ако вредност није број, враћа је непромењену.
    """
    if value in (None, ""):
        return NIJE_UPISANO
    try:
        n = int(value)
    except (TypeError, ValueError):
        return value
    if n < 1:
        return NIJE_UPISANO
    return _REDNI.get(n, f"{n}.")
