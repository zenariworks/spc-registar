"""Филтери: редни број речима — средњи род за „по реду", мушки за „брак"."""

from django import template

register = template.Library()

_REDNI = {
    1: "прво", 2: "друго", 3: "треће", 4: "четврто", 5: "пето",
    6: "шесто", 7: "седмо", 8: "осмо", 9: "девето", 10: "десето",
    11: "једанаесто", 12: "дванаесто", 13: "тринаесто", 14: "четрнаесто",
    15: "петнаесто", 16: "шеснаесто", 17: "седамнаесто", 18: "осамнаесто",
    19: "деветнаесто", 20: "двадесето",
}

_REDNI_M = {
    1: "први", 2: "други", 3: "трећи", 4: "четврти", 5: "пети",
    6: "шести", 7: "седми", 8: "осми", 9: "девети", 10: "десети",
    11: "једанаести", 12: "дванаести", 13: "тринаести", 14: "четрнаести",
    15: "петнаести", 16: "шеснаести", 17: "седамнаести", 18: "осамнаести",
    19: "деветнаести", 20: "двадесети",
}

NIJE_UPISANO = "није уписано"


def _redni(value, tabela):
    """Заједничка логика: број → реч из дате табеле редних бројева."""
    if value in (None, ""):
        return NIJE_UPISANO
    try:
        n = int(value)
    except (TypeError, ValueError):
        return value
    if n < 1:
        return NIJE_UPISANO
    return tabela.get(n, f"{n}.")


@register.filter
def redni_rec(value):
    """Број → редни број речима у средњем роду за „дете по реду" (1 → „прво").

    Празно/None или вредност < 1 → „није уписано"; ван опсега 1–20 → „N."
    (нпр. 21 → „21."); ако није број, враћа непромењено.
    """
    return _redni(value, _REDNI)


@register.filter
def redni_rec_m(value):
    """Број → редни број речима у мушком роду за „који брак" (1 → „први").

    Исто понашање као redni_rec, само мушки род (именица „брак" је мушка).
    """
    return _redni(value, _REDNI_M)
