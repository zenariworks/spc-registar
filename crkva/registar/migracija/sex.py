def infer_sex_from_name(ime: str | None) -> str | None:
    n = _normalize(ime)
    if not n:
        return None
    # If composite (e.g. "Марко Петар"), try the first token first then
    # try all tokens — return only if all tokens that hit agree.
    parts = [p for p in n.replace("-", " ").split() if p]
    if not parts:
        return None
    hits = set()
    for p in parts:
        if p in _MALE_NAMES:
            hits.add(MALE)
        elif p in _FEMALE_NAMES:
            hits.add(FEMALE)
    if len(hits) == 1:
        return next(iter(hits))
    return None


def infer_sex(ime: str | None = None, uloga: str | None = None) -> str | None:
    """Convenience: role first, then name."""
    return infer_sex_from_role(uloga) or infer_sex_from_name(ime)
