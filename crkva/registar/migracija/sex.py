"""Sex/gender inference helpers for legacy DBF data import.

The HramSP DBF schema has explicit sex only for baptism children (K_DETPOL).
For everything else we infer:
  * priests (HSPSVEST) → always М
  * weddings (HSPVENC) → role-based: bride row=Ж, groom row=М
  * household members (HSPUKUCANI) and household heads (HSPDOMACINI) →
    role hint first (мајка/отац/...), then name dictionary.

Returns 'М'/'Ж' on a confident hit, None on ambiguity.
"""

from __future__ import annotations

import unicodedata

MALE = "М"
FEMALE = "Ж"

# Cyrillic → Latin transliteration kept tiny; we normalize input to lowercase
# Cyrillic for matching, so dictionaries below are stored in cyrillic only.

_LAT_TO_CYR = str.maketrans(
    {
        "a": "а",
        "b": "б",
        "v": "в",
        "g": "г",
        "d": "д",
        "e": "е",
        "z": "з",
        "i": "и",
        "j": "ј",
        "k": "к",
        "l": "л",
        "m": "м",
        "n": "н",
        "o": "о",
        "p": "п",
        "r": "р",
        "s": "с",
        "t": "т",
        "u": "у",
        "f": "ф",
        "h": "х",
        "c": "ц",
    }
)


def _normalize(s: str | None) -> str:
    if not s:
        return ""
    s = unicodedata.normalize("NFC", s).strip().lower()
    s = s.translate(_LAT_TO_CYR)
    return s


