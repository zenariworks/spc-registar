"""Превод старих HSP сифри покретних празника у засебне покретне Slava редове.

У старом DBF-у (HSPSLAVE.DBF) покретни празници Васкршњег циклуса били су
„паркирани“ на фиксне датуме, сваки са својом сифром (SL_SIFRA). Поље
``DOM_RBRSL`` домаћинства чија је слава покретна показује управо на ту сифру.

Од #259 покретни празници живе као засебни редови (``pokretni=True``)
идентификовани помаком од Васкрса (``offset_dani``), без фиксног датума, а
``unos_slava`` им додељује PK по редоследу из ``slave.jsonl`` — који више
није једнак старој сифри. Зато се стара сифра преводи у одговарајући покретни
ред преко ``offset_dani`` (стабилан, независан од PK). Фиксне сифре и даље
важе као PK (поредак ``slave.jsonl`` прати SL_SIFRA — проверено тестом).

issue #255: без овог превода ``DOM_RBRSL`` покретне славе тихо показује на
фиксног свеца који је #259 вратио на тај датум.
"""

# legacy SL_SIFRA (HSPSLAVE.DBF) -> offset_dani одговарајућег покретног празника
POKRETNE_SLAVE_OFFSET_BY_SIFRA = {
    93: -8,  # Лазарева субота
    94: -7,  # Улазак Господа Исуса Христа у Јерусалим (Цвети)
    98: -3,  # Велики четвртак
    99: -2,  # Велики петак
    100: -1,  # Велика субота
    101: 0,  # Васкрс
    102: 1,  # Васкрски понедељак
    103: 2,  # Васкрсни уторак
    140: 39,  # Вазнесење Господње (Спасовдан)
    150: 49,  # Силазак Светог Духа на апостоле (Педесетница/Тројице/Духови)
    151: 50,  # Духовски понедељак
    152: 51,  # Духовски уторак
}


def resolve_slava(slava_uid, slava_model):
    """Врати исправан ``Slava`` ред за стару ``DOM_RBRSL`` сифру (или ``None``).

    Покретне сифре пресликава на покретни ред преко ``offset_dani`` (PK
    покретних редова није везан за стару сифру). За све остале сифра == PK
    фиксног празника.
    """
    if not slava_uid:
        return None
    offset = POKRETNE_SLAVE_OFFSET_BY_SIFRA.get(slava_uid)
    if offset is not None:
        return slava_model.objects.filter(pokretni=True, offset_dani=offset).first()
    return slava_model.objects.filter(uid=slava_uid).first()
